# Laço de repetição (existe for e while)
# while (condição) -> bloco de código repetido. Obrigatório que tenha uma variável início. Também é necessário uma atualização.
#você pode determinar quantas repetições terá, e também pode para-lo.

contador = 1
#enquanto (se condição for verdadeira): executa todo o bloco
#enquanto (se condição for falsa): para a execução de todo bloco
while (contador <= 10): #o contador vai até 10, menor e igual a 10.
    print(contador)
    contador = contador + 1
